using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Portal : MonoBehaviour
{
    // -----------------------------------------------------------------------------------------------------------------
    // P R O P E R T I E S
    // -----------------------------------------------------------------------------------------------------------------
    
    public Tile tile;
    public bool teleport;
    
    // -----------------------------------------------------------------------------------------------------------------
    // H E R I T E D  M E T H O D S
    // -----------------------------------------------------------------------------------------------------------------
    
    void Start()
    {
        
    }

    void Update()
    {
        
    }
    
    // -----------------------------------------------------------------------------------------------------------------
    // M E T H O D S
    // -----------------------------------------------------------------------------------------------------------------
    
    public bool TeleportUnit(Unit unit)
    {
        teleport = false;
        if (unit.tile == tile)
        {
            if (unit.hasChest)
                teleport = true;
        }

        return teleport;
    }
}
